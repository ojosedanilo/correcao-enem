def texto_gabarito_para_texto_js(texto_gabarito: str) -> str:
    gab = para_dict(texto_gabarito)

    return json.dumps(gab)


def para_dict(s: str) -> dict:
    linhas = s.strip().splitlines()
    return {linhas[i]: linhas[i + 1] for i in range(0, len(linhas), 2)}


def comparar_gabaritos(gab_str: str, meu_str: str, mostrar_erros=False):

    gab = para_dict(gab_str)
    meu = para_dict(meu_str)

    total = len(gab)
    acertos = sum(1 for k, v in gab.items() if k in meu and meu[k] == v)
    erros = total - acertos

    print(f"Total: {total}")
    print(f"Acertos: {acertos}")
    print(f"Erros: {erros}")
    print(f"Percentual de acertos: {acertos / total * 100:.2f}%")

    if mostrar_erros:
        print("\n--- Quest√µes erradas ---")
        for k, v in gab.items():
            if meu.get(k) != v:
                print(f"{k}: certo = {v}, seu = {meu.get(k)}")


